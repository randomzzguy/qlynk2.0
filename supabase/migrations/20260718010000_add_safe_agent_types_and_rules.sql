CREATE OR REPLACE FUNCTION public.save_agent_rule_config(
  p_owner_id UUID,
  p_agent_type TEXT,
  p_rules JSONB
)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $$
DECLARE
  next_version INTEGER;
BEGIN
  IF p_owner_id IS NULL OR p_rules IS NULL OR jsonb_typeof(p_rules) <> 'object' THEN
    RAISE EXCEPTION 'Invalid agent rule configuration';
  END IF;

  -- Serialize prompt-version allocation per owner, including the first save.
  -- Locking the durable auth row avoids duplicate version numbers from two
  -- dashboard tabs saving at the same time.
  PERFORM 1
  FROM auth.users
  WHERE id = p_owner_id
  FOR UPDATE;

  SELECT COALESCE(prompt_version, 0) + 1
  INTO next_version
  FROM public.agent_rule_configs
  WHERE user_id = p_owner_id;

  next_version := COALESCE(next_version, 1);

  INSERT INTO public.agent_rule_configs (
    user_id,
    purpose,
    audience,
    allowed_topics,
    blocked_topics,
    behavior_rules,
    forbidden_behaviors,
    uncertainty_message,
    escalation_message,
    custom_instructions,
    response_length,
    scope_mode,
    daily_message_limit,
    prompt_version,
    updated_at
  ) VALUES (
    p_owner_id,
    p_rules->>'purpose',
    COALESCE(p_rules->>'audience', ''),
    COALESCE(p_rules->'allowed_topics', '[]'::jsonb),
    COALESCE(p_rules->'blocked_topics', '[]'::jsonb),
    COALESCE(p_rules->'behavior_rules', '[]'::jsonb),
    COALESCE(p_rules->'forbidden_behaviors', '[]'::jsonb),
    p_rules->>'uncertainty_message',
    COALESCE(p_rules->>'escalation_message', ''),
    COALESCE(p_rules->>'custom_instructions', ''),
    COALESCE(p_rules->>'response_length', 'balanced'),
    COALESCE(p_rules->>'scope_mode', 'standard'),
    NULLIF(p_rules->>'daily_message_limit', '')::INTEGER,
    next_version,
    now()
  )
  ON CONFLICT (user_id) DO UPDATE SET
    purpose = EXCLUDED.purpose,
    audience = EXCLUDED.audience,
    allowed_topics = EXCLUDED.allowed_topics,
    blocked_topics = EXCLUDED.blocked_topics,
    behavior_rules = EXCLUDED.behavior_rules,
    forbidden_behaviors = EXCLUDED.forbidden_behaviors,
    uncertainty_message = EXCLUDED.uncertainty_message,
    escalation_message = EXCLUDED.escalation_message,
    custom_instructions = EXCLUDED.custom_instructions,
    response_length = EXCLUDED.response_length,
    scope_mode = EXCLUDED.scope_mode,
    daily_message_limit = EXCLUDED.daily_message_limit,
    prompt_version = next_version,
    updated_at = now();

  INSERT INTO public.agent_rule_config_versions (user_id, version, agent_type, rules)
  VALUES (p_owner_id, next_version, p_agent_type, p_rules);

  INSERT INTO public.agent_configs (user_id, agent_type, updated_at)
  VALUES (p_owner_id, p_agent_type, now())
  ON CONFLICT (user_id) DO UPDATE SET
    agent_type = EXCLUDED.agent_type,
    updated_at = now();

  RETURN next_version;
END;
$$;

REVOKE ALL ON FUNCTION public.save_agent_rule_config(UUID, TEXT, JSONB)
FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION public.save_agent_rule_config(UUID, TEXT, JSONB)
TO service_role;

