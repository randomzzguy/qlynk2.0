  user_id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  purpose TEXT NOT NULL,
  audience TEXT NOT NULL DEFAULT '',
  allowed_topics JSONB NOT NULL DEFAULT '[]'::jsonb,
  blocked_topics JSONB NOT NULL DEFAULT '[]'::jsonb,
  behavior_rules JSONB NOT NULL DEFAULT '[]'::jsonb,
  forbidden_behaviors JSONB NOT NULL DEFAULT '[]'::jsonb,
  uncertainty_message TEXT NOT NULL,
  escalation_message TEXT NOT NULL DEFAULT '',
  custom_instructions TEXT NOT NULL DEFAULT '',
  response_length TEXT NOT NULL DEFAULT 'balanced',
  scope_mode TEXT NOT NULL DEFAULT 'standard',
  daily_message_limit INTEGER,
  prompt_version INTEGER NOT NULL DEFAULT 1,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT agent_rule_configs_purpose_length CHECK (char_length(purpose) BETWEEN 1 AND 500),
  CONSTRAINT agent_rule_configs_audience_length CHECK (char_length(audience) <= 300),
  CONSTRAINT agent_rule_configs_allowed_topics_array CHECK (
    jsonb_typeof(allowed_topics) = 'array' AND jsonb_array_length(allowed_topics) <= 20
  ),
  CONSTRAINT agent_rule_configs_blocked_topics_array CHECK (
    jsonb_typeof(blocked_topics) = 'array' AND jsonb_array_length(blocked_topics) <= 20
  ),
  CONSTRAINT agent_rule_configs_behavior_rules_array CHECK (
    jsonb_typeof(behavior_rules) = 'array' AND jsonb_array_length(behavior_rules) <= 20
  ),
  CONSTRAINT agent_rule_configs_forbidden_behaviors_array CHECK (
    jsonb_typeof(forbidden_behaviors) = 'array' AND jsonb_array_length(forbidden_behaviors) <= 20
  ),
  CONSTRAINT agent_rule_configs_uncertainty_length CHECK (char_length(uncertainty_message) BETWEEN 1 AND 500),
  CONSTRAINT agent_rule_configs_escalation_length CHECK (char_length(escalation_message) <= 500),
  CONSTRAINT agent_rule_configs_custom_instructions_length CHECK (char_length(custom_instructions) <= 2000),
  CONSTRAINT agent_rule_configs_response_length_check CHECK (response_length IN ('concise', 'balanced', 'detailed')),
  CONSTRAINT agent_rule_configs_scope_mode_check CHECK (scope_mode IN ('standard', 'strict')),
  CONSTRAINT agent_rule_configs_daily_limit_check CHECK (
    daily_message_limit IS NULL OR daily_message_limit BETWEEN 10 AND 10000
  ),
  CONSTRAINT agent_rule_configs_prompt_version_check CHECK (prompt_version BETWEEN 1 AND 1000)
);

