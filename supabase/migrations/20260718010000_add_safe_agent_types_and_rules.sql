-- Replace the historical message trigger so usage is reserved atomically by
-- the server before an AI request rather than incremented after a race-prone
-- read/check sequence.
CREATE OR REPLACE FUNCTION public.update_conversation_message_count()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    UPDATE public.agent_conversations
    SET message_count = COALESCE(message_count, 0) + 1,
        updated_at = now()
    WHERE id = NEW.conversation_id;
  ELSIF TG_OP = 'DELETE' THEN
    UPDATE public.agent_conversations
    SET message_count = (
          SELECT count(*)
          FROM public.agent_messages
          WHERE conversation_id = OLD.conversation_id
        ),
        updated_at = now()
    WHERE id = OLD.conversation_id;
  END IF;
  RETURN NULL;
END;
$$;

CREATE OR REPLACE FUNCTION public.consume_agent_message_credit(
  p_owner_id UUID,
  p_message_limit INTEGER
)
RETURNS TABLE(allowed BOOLEAN, messages_used INTEGER, remaining INTEGER)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $$
DECLARE
  updated_count INTEGER;
  current_count INTEGER;
BEGIN
  IF p_owner_id IS NULL OR p_message_limit IS NULL OR p_message_limit <= 0 THEN
    RAISE EXCEPTION 'Invalid message credit parameters';
  END IF;

  UPDATE public.subscriptions
  SET messages_used = COALESCE(subscriptions.messages_used, 0) + 1
  WHERE user_id = p_owner_id
    AND COALESCE(subscriptions.messages_used, 0) < p_message_limit
  RETURNING subscriptions.messages_used INTO updated_count;

  IF updated_count IS NOT NULL THEN
    RETURN QUERY SELECT TRUE, updated_count, GREATEST(p_message_limit - updated_count, 0);
    RETURN;
  END IF;

  SELECT COALESCE(subscriptions.messages_used, 0)
  INTO current_count
  FROM public.subscriptions
  WHERE user_id = p_owner_id;

  RETURN QUERY SELECT FALSE, COALESCE(current_count, p_message_limit), 0;
END;
$$;

REVOKE ALL ON FUNCTION public.consume_agent_message_credit(UUID, INTEGER)
FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION public.consume_agent_message_credit(UUID, INTEGER)
TO service_role;
